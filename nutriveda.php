                                <p>It is also known PEM and is more commonly affected children between the ages of 6 months and 5 years. It has many short term and long term physical and mental effects such as growth retardation, lowered resistance to infections, increased mortality rates in young children. Two huge forms of protein energy malnutrition are marasmus and kwashiorkor. Marasmus results from starvation from long period of time and the affected child or adult becomes very thin and has most of the fat and muscle mass is expanded to provide energy. This is one of the most frequent forms of protein energy malnutrition conditions of severe food shortage. </p>

