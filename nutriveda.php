                                <p>Iodine is one of the most important micronutrient. When there is lack of iodine in the diet, it can lead to the iodine deficiency disorders, which can cause miscarriages, stillbirths, brain disorders, and retarded psychomotor development, hearing and speech impairments, and depleted levels of energy in children. This iodine deficiency is the most important and preventable cause of mental retardation across the globe and the diet is likely to be deficient in iodine where ever the soil content of iodine is no such as and mountainous region. Additionally, certain foods contain goitrogens, substances that inhibit iodine absorption and utilization in human body and need to be detoxifies before being consumed.  A simple measure to prevent iodine deficiency disorders is utilizing iodized salt in the diet.</p>

